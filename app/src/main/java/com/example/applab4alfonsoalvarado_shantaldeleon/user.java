public class user {

    public String email;
    public String pass;
    public String nombre;
    public String cedula;
    public String rol;

    public user(String email, String pass, String nombre, String cedula, String rol){
        this.email = email;
        this.pass = pass;
        this.nombre = nombre;
        this.cedula = cedula;
        this.rol = rol;
    }
}
